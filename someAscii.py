# ASCII = American Standard Code for Information Interchnage
''' Represent character as numeric values and also number vlaues as character
    A - Z = 65 - 90, a - z = 97 - 122
    0 - 9 = 48 - 57, space = 32
'''

# To check ASCII value of given character by using ord() function 
char = "f"
